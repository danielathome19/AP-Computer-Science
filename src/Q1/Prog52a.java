public class Prog52a {
    public static void main(String[] args) {
        int length = 143;
        int width = 80;
        width = 82;

        // + - * / %
        int area = length * width;
        int perim = 2 * length + 2 * width;

        System.out.println("Area = " + area);
        System.out.println("Perim = " + perim);
    }
}
